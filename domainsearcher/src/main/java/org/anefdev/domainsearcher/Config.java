package org.anefdev.domainsearcher;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

import java.util.Arrays;

@Configuration
public class Config {

    // Creates an object (bean) of DomainChecker which can be used and automatically injected into other beans
    @Bean
    public DomainChecker getDomainChecker() {
        return new DomainChecker(Arrays.asList("org", "de", "com"));
    }
}
